#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	int		cpt;

	cpt = 0;
	while (src[cpt])
		cpt++;
	if (dstsize == 0)
		return (cpt);
	i = 0;
	while (i < dstsize - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (cpt);
}
